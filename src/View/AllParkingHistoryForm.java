    private void btnViewAllActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewAllActionPerformed
        // TODO add your handling code here:
       try {
            ArrayList<ParkedVehicle>parkedVehiclesList= VehicleController.getAllParks();
            DefaultTableModel dtm = (DefaultTableModel) historyTable.getModel();
            dtm.setRowCount(0);
            for (ParkedVehicle parkedVehicles : parkedVehiclesList) {
                Object[] rowData={parkedVehicles.getvNum(),parkedVehicles.getArrival(),parkedVehicles.getDeparture(),parkedVehicles.getTotParkTime(),parkedVehicles.getTotFee(),parkedVehicles.getPayMethod()};
                dtm.addRow(rowData);
            }

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        } catch (ClassNotFoundException ex) {
            JOptionPane.showMessageDialog(this, "Driver not found...");
        }
    }//GEN-LAST:event_btnViewAllActionPerformed

